package com.example.b3rc;

public class TankModel {
    private String namaTank;
    private int gambarTank;
    private int tahun;

    public String getNamaTank() {
        return namaTank;
    }

    public void setNamaTank(String namaTank) {
        this.namaTank = namaTank;
    }

    public int getGambarTank() {
        return gambarTank;
    }

    public void setGambarTank(int gambarTank) {
        this.gambarTank = gambarTank;
    }

    public int getTahun() {
        return tahun;
    }

    public void setTahun(int tahun) {
        this.tahun = tahun;
    }
}
